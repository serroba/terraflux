"""Guard that the committed docs/ site data still matches the pipeline.

docs/crossings.parquet and docs/gates.json are generated by build_site.py but
committed (GitHub Pages deploys from the branch). Without this guard a fixture or
pipeline change could ship a stale, wrong-looking site with a green CI. These tests
fail if the committed artifacts drift from what the pipeline now produces.

Regenerate with: ``uv run generate_and_query.py && uv run build_site.py``.
"""

import unittest
from pathlib import Path

import duckdb

from build_site import gates_json

EXPERIMENT_DIR = Path(__file__).resolve().parent
REPO_ROOT = EXPERIMENT_DIR.parent.parent
DOCS_DIR = REPO_ROOT / "docs"
PARQUET = DOCS_DIR / "crossings.parquet"
GATES_JSON = DOCS_DIR / "gates.json"

# The deterministic per-gate daily aggregate the site must display, keyed by gate:
# (observed_crossings, laden_crossings, laden_capacity_dwt, observed_energy_gj).
EXPECTED = {
    "strait_of_hormuz": (5, 3, 1_020_000, 45_212_000),
    "strait_of_malacca": (4, 3, 740_000, 32_214_000),
}


class PublishedDataTest(unittest.TestCase):
    def test_gates_json_is_current(self) -> None:
        self.assertTrue(GATES_JSON.exists(), "docs/gates.json missing; build_site.py")
        self.assertEqual(
            GATES_JSON.read_text(),
            gates_json(),
            msg="docs/gates.json is stale; rerun build_site.py and commit",
        )

    def test_committed_parquet_reproduces_the_aggregate(self) -> None:
        self.assertTrue(PARQUET.exists(), "docs/crossings.parquet missing; build it")
        with duckdb.connect() as connection:
            rows = connection.execute(
                f"""
                SELECT
                    gate,
                    count(*),
                    count(*) FILTER (WHERE laden),
                    CAST(sum(capacity_dwt) FILTER (WHERE laden) AS BIGINT),
                    CAST(round(sum(energy_gj) FILTER (WHERE laden)) AS BIGINT)
                FROM read_parquet('{PARQUET.as_posix()}')
                WHERE event_date = DATE '2026-07-17'
                GROUP BY gate
                """
            ).fetchall()

        actual = {r[0]: (r[1], r[2], r[3], r[4]) for r in rows}
        self.assertEqual(actual, EXPECTED)


if __name__ == "__main__":
    unittest.main()
